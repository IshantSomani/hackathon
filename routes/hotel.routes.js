/**
 *  GET /hotel/seed-base
 * Inserts data from hotelsBase into DB
 */
router.get("/seed-base", async (req, res) => {
  try {
    // Get last S_No
    const lastHotel = await Hotel.findOne().sort({ S_No: -1 });
    let currentSno = lastHotel ? lastHotel.S_No : 0;

    const hotelsToInsert = hotelsBase.map((hotel) => {
      //  Random totalRooms (20120)
      const totalRooms = Math.floor(Math.random() * 101) + 20;

      //  Random vacancy (0  vacancy  totalRooms)
      const vacancy = Math.floor(Math.random() * (totalRooms + 1));

      //  Random rating if missing
      const rating =
        typeof hotel.Rating === "number"
          ? hotel.Rating
          : +(Math.random() * (5 - 3) + 3).toFixed(1);

      return {
        S_No: ++currentSno,
        Name: hotel.Name,
        Address: hotel.Address,
        Phone: hotel.Phone,
        Website: hotel.Website,
        Rating: rating,
        Reviews: hotel.Reviews ?? null,
        City: hotel.City,
        category: hotel.category || "Hotel",
        nearbyPlaces: hotel.nearbyPlaces || [],
        totalRooms,
        vacancy,
      };
    });

    await Hotel.insertMany(hotelsToInsert);

    res.json({
      success: true,
      inserted: hotelsToInsert.length,
      message: "Hotels seeded successfully with random rooms & rating",
    });
  } catch (err) {
    res.status(500).json({
      success: false,
      error: err.message,
    });
  }
});

/**
 *  PUT /hotel/update-vacancy
 * Auto update ALL hotels every 30 sec (one-time trigger)
 */
let vacancyInterval = null;

router.put("/update-vacancy", async (req, res) => {
  if (vacancyInterval) {
    return res.json({
      success: false,
      message: "Vacancy updater already running",
    });
  }

  vacancyInterval = setInterval(async () => {
    try {
      const hotels = await Hotel.find();

      for (const hotel of hotels) {
        const change = Math.floor(Math.random() * 5) - 2;
        let newVacancy = hotel.vacancy + change;

        newVacancy = Math.max(0, Math.min(newVacancy, hotel.totalRooms));

        hotel.vacancy = newVacancy;
        await hotel.save();
      }

      console.log(" Vacancy auto-updated");
    } catch (err) {
      console.error("Vacancy update error:", err.message);
    }
  }, 30000);

  res.json({
    success: true,
    message: "Auto vacancy update started (30s)",
  });
});

/**
 *  GET /hotel/list
 * Table-ready data
 */
router.get("/list", async (req, res) => {
  try {
    const {
      city,
      category,
      minRating,
      maxRating,
      minVacancy,
      maxVacancy,
      name,
      nearbyPlace,
      sortBy = "S_No",
      order = "asc",
      page = 1,
      limit = 20,
    } = req.query;

    const filter = {};

    //  City filter
    if (city) filter.City = city;

    //  Category filter
    if (category) filter.category = category;

    //  Rating range
    if (minRating || maxRating) {
      filter.Rating = {};
      if (minRating) filter.Rating.$gte = Number(minRating);
      if (maxRating) filter.Rating.$lte = Number(maxRating);
    }

    //  Vacancy range
    if (minVacancy || maxVacancy) {
      filter.vacancy = {};
      if (minVacancy) filter.vacancy.$gte = Number(minVacancy);
      if (maxVacancy) filter.vacancy.$lte = Number(maxVacancy);
    }

    //  Name search (case-insensitive)
    if (name) {
      filter.Name = { $regex: name, $options: "i" };
    }

    //  Nearby place search
    if (nearbyPlace) {
      filter.nearbyPlaces = { $in: [nearbyPlace] };
    }

    //  Sorting
    const sortOrder = order === "desc" ? -1 : 1;
    const sortOptions = { [sortBy]: sortOrder };

    //  Pagination
    const skip = (Number(page) - 1) * Number(limit);

    const [hotels, total] = await Promise.all([
      Hotel.find(filter).sort(sortOptions).skip(skip).limit(Number(limit)),
      Hotel.countDocuments(filter),
    ]);

    res.json({
      success: true,
      count: hotels.length,
      total,
      page: Number(page),
      limit: Number(limit),
      data: hotels,
    });
  } catch (err) {
    res.status(500).json({ success: false, error: err.message });
  }
});
/**
 *  GET /hotel/analytics/city
 * City-wise analytics dashboard
 */
router.get("/analytics/city", async (req, res) => {
  try {
    const analytics = await Hotel.aggregate([
      // Group by City + Category
      {
        $group: {
          _id: {
            city: "$City",
            category: "$category",
          },
          count: { $sum: 1 },
          avgRating: { $avg: "$Rating" },
          totalRooms: { $sum: { $ifNull: ["$totalRooms", 0] } },
          totalVacancy: { $sum: { $ifNull: ["$vacancy", 0] } },
        },
      },

      // Group again by City
      {
        $group: {
          _id: "$_id.city",
          totalHotels: { $sum: "$count" },
          avgRating: { $avg: "$avgRating" },
          totalRooms: { $sum: "$totalRooms" },
          totalVacancy: { $sum: "$totalVacancy" },
          categories: {
            $push: {
              category: "$_id.category",
              count: "$count",
            },
          },
        },
      },

      // Calculate occupancy %
      {
        $addFields: {
          occupancyPercent: {
            $cond: [
              { $eq: ["$totalRooms", 0] },
              0,
              {
                $round: [
                  {
                    $multiply: [
                      {
                        $divide: [
                          { $subtract: ["$totalRooms", "$totalVacancy"] },
                          "$totalRooms",
                        ],
                      },
                      100,
                    ],
                  },
                  1,
                ],
              },
            ],
          },
        },
      },

      // Final shape
      {
        $project: {
          _id: 0,
          city: "$_id",
          totalHotels: 1,
          avgRating: { $round: ["$avgRating", 2] },
          totalRooms: 1,
          totalVacancy: 1,
          occupancyPercent: 1,
          categories: 1,
        },
      },

      // Sort by hotel count
      { $sort: { totalHotels: -1 } },
    ]);

    res.json({
      success: true,
      data: analytics,
    });
  } catch (err) {
    res.status(500).json({
      success: false,
      error: err.message,
    });
  }
});

